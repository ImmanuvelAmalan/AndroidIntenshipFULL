class DataTypes{
	public static void main(String args[]){
		int intnum=10;   // int
		byte bytenum=20; // byte
		short shortnum=30; // short
		long longnum=979060634; // long
		float floatnum=242.23f; //float
		double doublenum=24214.242345221f; // double
		boolean bool=true; // boolean
		char ch='A'; // char
		System.out.println("Integer: "+intnum);
		System.out.println("Byte: "+bytenum);
		System.out.println("Short: "+shortnum);
		System.out.println("Long: "+longnum);
		System.out.println("Float: "+floatnum);
		System.out.println("Double: "+doublenum);
		System.out.println("Boolean: "+bool);
		System.out.println("Character: "+ch);
		
	}
}